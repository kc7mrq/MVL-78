<span style="color:#000000;font-family:Arial;font-size:13px;">No one knows with certainty how or when the Masonic Fraternity was formed. A widely accepted theory among Masonic scholars is that it arose from the stonemasons' guilds during the Middle Ages. The language and symbols used in the fraternity's rituals come from this era. The oldest document that makes reference to Masons is the Regius Poem, printed about 1390, which was a copy of an earlier work. In 1717, four lodges in London formed the first Grand Lodge of England, and records from that point on are more complete.<br><br>Within thirty years, the fraternity had spread throughout Europe and the American Colonies. Freemasonry became very popular in colonial America. George Washington was a Mason, Benjamin Franklin served as the head of the fraternity in Pennsylvania, as did Paul Revere and Joseph Warren in Massachusetts. Other well-known Masons involved with the founding of America included John Hancock, John Sullivan, Lafayette, Baron Fredrick von Stuben, Nathanael Greene, and John Paul Jones. Another Mason, Chief Justice John Marshall, shaped the Supreme Court into its present form.<br><br>Over the centuries, Freemasonry has developed into a worldwide fraternity emphasizing personal study, self-improvement, and social betterment via individual involvement and philanthropy. During the late 1700s it was one of the organizations most responsible for spreading the ideals of the Enlightenment: the dignity of man and the liberty of the individual, the right of all persons to worship as they choose, the formation of democratic governments, and the importance of public education. Masons supported the first public schools in both Europe and America.<br><br>During the 1800s and early 1900s, Freemasonry grew dramatically. At that time, the government had provided no social &quot;safety net&quot;. The Masonic tradition of founding orphanages, homes for widows, and homes for the aged provided the only security many people knew.<br><br>Today in North America, the Masonic Fraternity continues this tradition by giving almost $1.5 million each day to causes that range from operating children's hospitals, providing treatment for childhood language disorders, treating eye diseases, funding medical research, contributing to local community service, and providing care to Masons and their families at Masonic Homes.<br><br>The four million Masons worldwide continue to help men and women face the problems of the 21st century by building bridges of brotherhood and instilling in the hearts of men ideals for a better tomorrow.<br></span></div>
